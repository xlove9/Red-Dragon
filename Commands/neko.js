let DataHelp = DataPack.constdata
let WorkType = Details.WORKTYPE == 'public' ? false : true

let DATA = DataHelp.dataGet('nekobin');
    SewQueen['IntroduceCMD']({pattern: 'neko', fromMe: WorkType, desc: DATA.NEKO_DESC}, (async (message, input) => {

        if (!message.reply_message) return await message.sendMessage(DATA.NEED_REPLY);
        if (!message.reply_message.text) return await message.sendMessage(DATA.MUST_TEXT);
        let base_URI = "https://nekobin.com/api/documents";
        try {
            let response = await got.post(base_URI, {json : {content : message.reply_message.text}}); 
            json = JSON.parse(response.body);
            neko_url = 'https://nekobin.com/' + json.result.key;
            await message.reply(neko_url);        
        } catch (err) {
            await message.reply(err.message, MessageType.text);
        }
       
    }));
    
